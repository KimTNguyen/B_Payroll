package santarosa.edu.homework;

public class Employee {
	private String name;

	/**
	 * Gets name of the employee
	 * 
	 * @return the employee's name
	 */
	public String getName() {
		return name;
	}

	/**
	 * Sets name for the employee
	 * 
	 * @param name
	 *            the employee's name
	 */
	public void setName(String name) {
		this.name = name;
	}
}
